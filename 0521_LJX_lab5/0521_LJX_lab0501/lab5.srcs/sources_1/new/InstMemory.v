module InstMemory(
    input [31:0] PC,
    input reset,
    output reg [31:0] INST
    );
    
    reg [31:0] InstFile [0:255];
    initial begin
//        $readmemb("D:/cs145/mem_inst.txt", InstFile);
        InstFile[0]=32'b00001000000000000000000000000001;//j 1
		InstFile[1]=32'b10001100000000010000000000000001;//lw $1,1($0)
		InstFile[2]=32'b10001100000000100000000000000011;//lw $2,3($0)
		InstFile[3]=32'b00000000001000100001100000100000;//add $3,$1,$2
		InstFile[4]=32'b00000000010000010010000000100010;//sub $4,$2,$1
		InstFile[5]=32'b00000000011001000010100000101010;//slt $5,$3,$4
		InstFile[6]=32'b00000000001000100011000000100100;//and $6,$1,$2
		InstFile[7]=32'b00000000001000100011100000100101;//or $7,$1,$2
		InstFile[8]=32'b10101100000000110000000000000001;//sw $3,1($0)
		InstFile[9]=32'b10101100000001000000000000000010;//sw $4,2($0)
		InstFile[10]=32'b10001100000010000000000000000001;//lw $8,1($0)
		InstFile[11]=32'b10001100000000010000000000000010;//lw $1,2($0)
		InstFile[12]=32'b00010000011010000000000000000001;//beq $8,$2,1
		InstFile[13]=32'b00001000000000000000000000000000;//j 0
		InstFile[14]=32'b00001000000000000000000000000001;//j 1  
    end
    always @ (reset)
    begin
        InstFile[0]=32'b00001000000000000000000000000001;//j 1
		InstFile[1]=32'b10001100000000010000000000000001;//lw $1,1($0)
		InstFile[2]=32'b10001100000000100000000000000011;//lw $2,3($0)
		InstFile[3]=32'b00000000001000100001100000100000;//add $3,$1,$2
		InstFile[4]=32'b00000000010000010010000000100010;//sub $4,$2,$1
		InstFile[5]=32'b00000000011001000010100000101010;//slt $5,$3,$4
		InstFile[6]=32'b00000000001000100011000000100100;//and $6,$1,$2
		InstFile[7]=32'b00000000001000100011100000100101;//or $7,$1,$2
		InstFile[8]=32'b10101100000000110000000000000001;//sw $3,1($0)
		InstFile[9]=32'b10101100000001000000000000000010;//sw $4,2($0)
		InstFile[10]=32'b10001100000010000000000000000001;//lw $8,1($0)
		InstFile[11]=32'b10001100000000010000000000000010;//lw $1,2($0)
		InstFile[12]=32'b00010000011010000000000000000001;//beq $8,$2,1
		InstFile[13]=32'b00001000000000000000000000000000;//j 0
		InstFile[14]=32'b00001000000000000000000000000001;//j 1        
    end
    always @(PC) 
    begin
    INST=InstFile[PC>>2];
    end
endmodule
